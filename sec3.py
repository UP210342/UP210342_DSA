#Operators - data manipulation tools

entero = 7 // 2 #rounding goes to the lower value 
print(entero)

residuo = 7 % 2 
print(residuo)

potencia = 2**3
print(potencia)

raiz = 16 ** .5
print(raiz)

print(12 % 4.5)

print( -4 - -4) #unary operator (signo de un numero)
print( 9 % 6 % 2 ) #left sided binding 
print(2 ** 2 ** 3) #right sided binding
